        <h2>O sucesso em um passe de mágica</h2>

        <p>
            Ainda em 1970, o Sabbath lança “Paranoid” considerado o maior sucesso comercial da banda. Com faixas que
            abordam temas variados, como política, a guerra do Vietnã , ficção científica e efeitos de abuso de
            drogas, a banda vendeu cinco mil cópias do disco no dia de lançamento e permaneceu nas paradas por
            semanas a fio e rendendo uma turnê norte-americana.
            O Sabbath continua colecionando sucessos, discos e turnês nos dois álbuns seguintes ( um deles, o V.4
            considerado o melhor disco do Sabbath por muitos). E em 1976, com a banda já exausta de turnês, rotina
            de estúdio e troca de empresários, o Sabbath lança o disco Techical Ecstasy muito mal recebido por
            todos, inclusive por Ozzy Osbourne. Nessa época ele já passava por sérios problemas pessoais e com o
            Sabbath, que culminaria na sua saída no final da década e na entrada de Ronnie James Dio.
        </p>

        <h2>Era Dio /Gillan</h2>

        <p>
            Em 1980 é lançado Heaven And Hell, com Dio já como vocalista e foi aprovado pelos fãs. E a partir daí
            começa a dança sabática de cadeiras na banda, pois em 1981, o baterista Bill Ward deixa a banda,
            entrando Vinnie Appice em seu lugar.
            Em 1983, o Sabbath lança um disco ao vivo “Live Evil” que causou várias polêmicas, pois Ozzy reclamou de
            seu vocal não ter sido incluído nas faixas. Além disso, Dio e Iommi se desentenderam por causa da
            mixagem do disco, o que levou Dio e o baterista Appice a deixar o Sabbath. Para o seu posto, Ian Gillan
            ( ex- Deep Purple) foi o escolhido, e dizem que ele aceitou a proposta quando estava bêbado. Com essa
            formação, o “Black Purple”, apelido recebido pelos críticos, lançou um disco mais maduro e consistente,
            embora não bem aceito, chamado “Born Again” em 1983. Alguns meses depois, Ian Gillan anuncia que o Deep
            Purple está de volta. Ozzy volta para a banda, mas apenas se apresentou com o Sabbath na formação
            orginal para o Live Aid em 1985.
            A segunda metade da década de 80 é mais sombria do que a banda tocava em suas canções. Iommi se sente
            perdido pressionado a gravar mais um álbum, mas sem vocalista. O americano Ray Gillen entra para o
            Sabbath em 1987 mas logo sai da banda sem gravar nenhum disco, falecendo de AIDS em 1993. Tony Martin
            assume os vocais em uma formação que só tem Iommi como membro original do Sabbath. A década de 90 inicia
            com o disco TYR, sem muita relevância e depois de outras mudanças na formação, Dio retorna aos vocais.
        </p>

