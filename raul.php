        <h2>Como ele surgiu</h2>

        <p>
            Nascido em Salvador, no dia 28 de junho de 1945, Raul Santos Seixas veio ao mundo predestinado a ser a
            eterna Metamorfose Ambulante que revolucionou o rock and roll e colocou muita gente pra pensar.

            Mesmo depois de 30 anos de sua morte, os fãs ainda buscam compreender essa figura icônica e muito respeitada
            no meio da música.

            A carreira de Raul começou no início da década de 60, quando formou a banda Relâmpagos do Rock, que depois
            de algumas apresentações e de conquistar visibilidade em Salvador, mudou de nome para Os Panteras.
            Mesmo com a gravação do primeiro disco, em 1968, Os Panteras não conquistaram o sucesso esperado e Raul
            começou a passar dificuldades no Rio de Janeiro.
        </p>

        <p>
            Nesta época ele vivia em Ipanema e ia caminhando até o centro para divulgar seu disco, chegou a passar fome
            e a sua experiência no Rio serviu de inspiração para a música Ouro de Tolo, que viria a fazer sucesso mais
            tarde, com o cantor já em carreira solo.

            Vendo que as coisas não estavam indo bem, ele voltou para Salvador e praticamente se trancou no quarto com
            os livros de filosofia e misticismo.
        </p>

        <h2>O místico Raul, um eterno buscador </h2>

        <p>
            Boa parte da inspiração de Raul para escrever vinha de seu interesse pelo ocultismo, filosofia, psicologia e
            pela literatura de forma geral.

            Desde pequeno, se encantou pela biblioteca vasta de seu pai e passava horas trancado dentro do quarto lendo,
            acabou desenvolvendo até miopia.
        </p>

        <p>Autores como Lao-Tse, Nietzsche, George Orwell, Aldous Huxley e o ocultista Aleister Crowley eram os
            preferidos do cantor.

