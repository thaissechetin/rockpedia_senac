        <h1 class="text-center">Legião Urbana</h1>

        <p>A Legião Urbana surgiu no final de 1982 quando Renato Russo juntou-se a Marcelo Bonfá, Eduardo Paraná
            (Hoje, Kadu Lambach) e Paulo Guimarães (o `Paulista`). Em 1983, Paulista e Paraná saem e Dado
            Villa-Lobos assume a guitarra. Herbert Viana e Bi Ribeiro, este último ex-aluno de inglês de Renato em
            Brasília, integravam a banda Paralamas do Sucesso (que já estava no elenco da EMI-Odeon) e indicaram
            àquela gravadora a fita demo da nova banda de Brasília. Depois de a gravação chegar às mãos de
            executivos da EMI-Odeon, a Legião Urbana foi contratada e lançou seu primeiro álbum, em 1985, emplacando
            em junho daquele ano as canções “Será”, “Ainda é Cedo” e “Geração Coca-Cola”.</p>

        <p>
            A
            Revista Bizz, leitura obrigatória para os amantes da música daquela época,
            elegia a Legião como a melhor banda e Renato, o melhor cantor daquele ano.
            Renato Rocha, o `Negrete` entra para a Legião como baixista, pouco antes da
            gravação do primeiro disco, mas já depois de a banda ter feito os célebres
            shows no Circo Voador, no Rio de Janeiro, e no Napalm em São Paulo. O maior
            sucesso foi “Eduardo e Mônica”, que conta a história de dois jovens que se
            apaixonam apesar dos estilos diferentes de vida. A Legião Urbana divulgava um
            conteúdo que qualquer jovem brasileiro dos anos 80 compreendia e se identificava.
        </p>

        <p>
            Em
            dezembro de 1986, uma grande plateia segue a turnê do álbum que vinha fazendo
            muito sucesso. Já em 1987 sai o terceiro álbum, Que País É Este, estourando o
            improvável mega-hit “Faroeste Caboclo”, considerada inicialmente muito grande
            (nove minutos) para ocupar a faixa radiofônica, o que logo caiu por terra. A
            música conta a saga do brasileiro João do Santo Cristo, um personagem criado
            por Renato e que chegou às telonas no ano de 2013, quando a letra foi adaptada
            para o roteiro do filme homônimo. E depois de muita espera chega às lojas As
            Quatro Estações, álbum que inicia a fase mais madura da Legião.
        </p>

        <p>
            Nove
            das onze músicas do álbum tocam no rádio e rapidamente o disco se torna o mais
            vendido da carreira da banda, com quase dois milhões de cópias apenas no ano de
            lançamento. Já “Vento no Litoral” foi composta por Renato para o ex-namorado
            americano Scott, quando ele voltou para sua terra natal após morar com ele
            alguns meses no Brasil. Em 1992 o disco duplo Música Para Acampamentos foi
            montado pelo próprio Russo e pela EMI, na intenção de suprir os desejos por
            novidades musicais da banda. E a Legião só entraria em estúdio e lançaria um
            álbum de inéditas no ano seguinte.
        </p>

        <p>
            Só
            dois discos solo depois (The Stonewall Celebration Concert e Equilibrio
            Distante) e em 1996 Renato viria a trabalhar novamente em material inédito da
            Legião Urbana, meio que “correndo contra o tempo”. Assim surgiram A Tempestade
            ou O Livro Dos Dias, lançado um mês antes de sua morte, e Uma Outra Estação,
            primeiro álbum póstumo, editado em 1997. Os álbuns trazem obras como “1º de
            Julho”, que Renato fez para Cássia Eller quando estava grávida, “Dezesseis” e
            “A Via Láctea”, essas duas também hits radiofônicos. E resgates como
            “Clarisse”, que já deveria ter sido lançada antes, mas acabava sempre de fora
            dos álbuns oficiais.
        </p>

        <p>
            O
            ano de 1998 é marcado pelo lançamento da compilação Mais do Mesmo, uma
            coletânea com os maiores sucessos da banda.



            E os anos 90 foram definitivos para o Legião: Renato Russo, um dos maiores nomes da música brasileira e
            fundador da banda de rock Legião Urbana, nos deixou no dia 11 de outubro de 1996, aos 36 anos, após
            complicações decorrentes da Aids. Após sua morte, Dado Villa-Lobos e Marcelo Bonfá decidiram encerrar as
            atividades da banda.
